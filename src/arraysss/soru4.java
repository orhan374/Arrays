package arraysss;

public class soru4 {
    public static void main(String[] args) {
        for (int i = 2; i > -1; i--) {
            switch (i) {
                case 0:
                    System.out.print("k");
                case 1:
                    System.out.print("l");
                default:
                    System.out.print("m");
            }
        }
    }
}